    public String maleCalculation(double standardDrinks, double weightSlider, double drinkingSlider) {
        
            weightSlider = weightSlider * 6.8;

            String howDrunk = Double.toString(((standardDrinks * 10) - (7.5 * drinkingSlider)) / weightSlider);
            
            return(howDrunk);
            
    }
    
    public String femaleCalculation(double standardDrinks, double weightSlider, double drinkingSlider) {
        
                    weightSlider = weightSlider * 5.5; 
                
            String howDrunk = Double.toString(((standardDrinks * 10) - (7.5 * drinkingSlider)) / weightSlider);
            
            return(howDrunk);
    }
