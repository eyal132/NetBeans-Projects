    /**
     * When Calculate button is pressed the code gets the number of drinks had
     * from the text box and calculates it to how drunk they are. It then pastes
     * it back to a label on the GUI for the user to read.
     *
     * @param evt
     */
    
    public String genderChoice = "";

    private void calculationButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_calculationButtonActionPerformed
        
        
        try {

            String numberOfDrinks = userInput.getText();

            double standardDrinks = Double.parseDouble(numberOfDrinks);
            
            double drinkingSlider = drunkSlider.getValue();
            
            double weightSlider = weight.getValue();
            
            if (genderChoice == "Male") {
                
                drunkLabel.setText(maleCalculation(standardDrinks, weightSlider, drinkingSlider) + "% in blood");

            } else { 
   
                drunkLabel.setText(femaleCalculation(standardDrinks, weightSlider, drinkingSlider) + "% in blood");
                
            }
        } catch (NumberFormatException ex) {

            drunkLabel.setText("Do not type words in the text box!!");

        }

    }//GEN-LAST:event_calculationButtonActionPerformed

